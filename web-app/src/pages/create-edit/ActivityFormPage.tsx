import { useEffect, useState } from 'react';
import { useNavigate, useParams } from 'react-router';
import { Link } from 'react-router-dom';
import { Button, Header, Segment } from 'semantic-ui-react';
import { Formik, Form } from 'formik';
import * as Yup from 'yup';
import { useAppDispatch, useAppSelector } from '../../store/hooks';
import { selectCreatingStatus } from '../../store/activities/selectors';
import { Activity } from '../../store/activities/model/activity';
import {
  createOrEditActivityAsync,
  getActivityAsync,
} from '../../store/activities/actions.thunk';
import { MyTextInput } from '../../components/form/MyTextInput';
import { MyTextArea } from '../../components/form/MyTextArea';
import { MySelectInput } from '../../components/form/MySelectInput';
import { CATEGORY_OPTIONS } from '../../utils/constants';
import { MyDateInput } from '../../components/form/MyDateInput';

export default function ActivityFormPage() {
  const { id } = useParams();
  const navigate = useNavigate();

  const dispatch = useAppDispatch();
  const creatingStatus = useAppSelector(selectCreatingStatus);
  const [activity, setActivity] = useState<Activity>({
    id: '',
    title: '',
    category: '',
    description: '',
    date: null,
    city: '',
    venue: '',
  });

  const validationSchema = Yup.object({
    title: Yup.string().required('The activity title is required'),
    category: Yup.string().required('The activity category is required'),
    description: Yup.string().required('The activity description is required'),
    date: Yup.string().required('The activity date is required'),
    city: Yup.string().required('The activity city is required'),
    venue: Yup.string().required('The activity venue is required'),
  });

  useEffect(() => {
    if (id) {
      dispatch(getActivityAsync(id)).then((activity) => setActivity(activity!));
    }
  }, [id, dispatch]);

  function handleFormSubmit(activity: Activity) {
    dispatch(createOrEditActivityAsync(activity)).then((activity) =>
      navigate(`/activities/${activity!.id}`)
    );
  }

  // function handleChange(
  //   event: ChangeEvent<HTMLInputElement | HTMLTextAreaElement>
  // ) {
  //   const { name, value } = event.target;
  //   setActivity({ ...activity, [name]: value });
  // }

  return (
    <Segment clearing>
      <Header content='Activity Details' sub color='teal' />
      <Formik
        enableReinitialize
        initialValues={activity}
        validationSchema={validationSchema}
        onSubmit={(values) => handleFormSubmit(values)}
      >
        {({ handleSubmit, isValid, isSubmitting, dirty }) => (
          <Form className='ui form' onSubmit={handleSubmit}>
            <MyTextInput placeholder='Title' name='title' />
            <MyTextArea placeholder='Description' name='description' rows={3} />
            <MySelectInput
              options={CATEGORY_OPTIONS}
              placeholder='Category'
              name='category'
            />
            <MyDateInput
              placeholderText='Date'
              name='date'
              showTimeSelect
              timeCaption='time'
              dateFormat='MMMM d, yyyy h:mm aa'
            />
            <Header content='Location Details' sub color='teal' />
            <MyTextInput placeholder='City' name='city' />
            <MyTextInput placeholder='Venue' name='venue' />
            <Button
              disabled={isSubmitting || !dirty || !isValid}
              loading={creatingStatus === 'loading'}
              floated='right'
              positive
              type='submit'
              content='Submit'
            />
            <Button
              as={Link}
              to='/activities'
              floated='right'
              type='button'
              content='Cancel'
            />
          </Form>
        )}
      </Formik>
    </Segment>
  );
}
